package org.palindrome.business;


/**
 * Service qui supporte la détection de palindromes dans un texte.
 * @author tarik
 */
public interface IPalindromeService {
	
	/**
	 * Service de détection de palindromes dans un texte.
	 * @param textPathFile le chemin absolu du fichier texte brut sur le systeme
	 * @return true si le texte contient un palindrome et false sinon
	 */
	boolean containsPalindrome(String textPathFile);
	
}
